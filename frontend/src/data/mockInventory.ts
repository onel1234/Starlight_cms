// Helper function to get low stock products
export const getLowStockProducts = (): Product[] => {
  return mockProducts.filter(product => product.stockQuantity <= product.minimumStock)
}

// Helper function to get products by category
export const getProductsByCategory = (categoryId: number): Product[] => {
  return mockProducts.filter(product => product.categoryId === categoryId)
}

// Helper function to get stock movements for a product
export const getStockMovementsByProduct = (productId: number): StockMovement[] => {
  return mockStockMovements.filter(movement => movement.productId === productId)
}